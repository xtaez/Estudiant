package cubesystem3d;

/**
 *
 * @author bernat
 */
public class RainMan {

    private int day;
    private int month;
    private int year;
    private int mmRain;
    
    public RainMan() {
    }

    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getMmRain() {
        return mmRain;
    }

    public void setMmRain(int mmRain) {
        this.mmRain = mmRain;
    }
    
    
    
}
